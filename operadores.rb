# Declaração de variaveis
a, b = [1,2]

# if normal
if a > b
    puts "A é maior que B"
else
    puts "B é maior que A"
end

# if de uma linha
# Só apresenta valor quando a condição for verdadeira
puts "A é menor que B" if a < b


