nome = "  Guilherme   "

# Deixa tudo minusculo
puts nome.downcase

# Deixa tudo maisculo
puts nome.upcase

# Remove espaços em branco no começo e no final
puts nome.strip

# Substituição de strings
puts nome["Guilherme"] = "Gui"

veiculo = "jeep renegade"

# Deixa primeira letra maiscula
puts veiculo.capitalize

# Mostra quantidade de caracteres
puts veiculo.chars

# Separa a variavel por espaços
# ou por valor determinado
puts veiculo.split
puts veiculo.split("e")